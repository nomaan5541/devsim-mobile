  List<List<int>> generateArtGrid(String message) {
    final cleanMsg = message.toUpperCase();
    final List<List<int>> grid = List.generate(7, (_) => []);

    if (cleanMsg.isEmpty) return grid;

    for (int charIdx = 0; charIdx < cleanMsg.length; charIdx++) {
      final char = cleanMsg[charIdx];
      final charMatrix = _font[char] ?? _font[' '];

      for (int row = 0; row < 7; row++) {
        grid[row].addAll(charMatrix![row]);
        // Add spacing column if not the last character
        if (charIdx < cleanMsg.length - 1) {
          grid[row].add(0);
        }
      }
    }
    return grid;
  }

  DateTime getSundayOfWeek(DateTime date) {
    if (date.weekday == 7) {
      return DateTime(date.year, date.month, date.day);
    }
    return DateTime(date.year, date.month, date.day).subtract(Duration(days: date.weekday));
  }

  List<Map<String, dynamic>> mapGridToDates(List<List<int>> grid, {DateTime? endOffset}) {
    final List<Map<String, dynamic>> mapList = [];
    if (grid.isEmpty || grid[0].isEmpty) return mapList;

    final targetEnd = endOffset ?? DateTime.now();
    final currentSunday = getSundayOfWeek(targetEnd);
    final int width = grid[0].length;
    
    // Map grid columns to weeks. Last column aligns with the Sunday of currentSunday.
    final startSunday = currentSunday.subtract(Duration(days: (width - 1) * 7));

    for (int col = 0; col < width; col++) {
      for (int row = 0; row < 7; row++) {
        final val = grid[row][col];
        if (val == 1) {
          final pixelDate = startSunday.add(Duration(days: col * 7 + row));
          mapList.add({
            'date': pixelDate,
            'dateString': pixelDate.toUtc().toIso8601String().split('T')[0] + 'T12:00:00Z',
          });
        }
      }
    }
    return mapList;
  }
